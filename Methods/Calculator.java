package Methods;

public class Calculator {
    public static int addition(int num1,int num2)
    {
        return num1+num2;
    }
    public static int addition(int num1,int num2,int num3)
    {
        return num1+num2+num3;
    }
    public static int addition(int num1,int num2,int num3,int num4)
    {
        return num1+num2+num3+num4;
    }
    public static int subtraction(int num1,int num2)
    {
        return num1-num2;
    }
    public static int multiplication(int num1,int num2)
    {
        return num1*num2;
    }
    public static int multiplication(int num1,int num2,int num3)
    {
        return num1*num2*num3;
    }
    public static int multiplication(int num1,int num2,int num3,int num4)
    {
        return num1*num2*num3*num4;
    }

    public static int division(int num1,int num2)
    {
        return num1/num2;
    }
}
