package basics;

public class ComparisonExapmple {
    public static void main(String[] args) {
        int x=10;
        int y=12;
        int z=10;
        System.out.println(x==z); //true
        System.out.println(x!=y); //true
        System.out.println(x<y); //true
        System.out.println(x>z); //false
        System.out.println(y<=z); //false
        System.out.println(x>=z); //true

    }
}
