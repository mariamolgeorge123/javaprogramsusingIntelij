package AssignmentUsingMethods;

public class DisplayFirst10NaturalForloop {
    public void printdata()
    {

        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }
    }
    public static void main(String[] args) {
        DisplayFirst10NaturalForloop datas=new DisplayFirst10NaturalForloop();
        datas.printdata();

    }
}
